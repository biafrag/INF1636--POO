package Tabuleiro;

public class Pair {

	int x;
	int y;
	public Pair()
	{
		x=0;
		y=0;
	}
	public Pair(int valueX,int valueY)
	{
		x=valueX;
		y=valueY;
	}
	public void setX(int value)
	{
		x=value;
	}
	public void setY(int value)
	{
		y=value;
	}
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	
}
